#დაწერეთ პროგრამა, რომელიც მომხარებელს შემოატანინებს რიცხვს და დაბეჭდავს ეს რიცხვი დადებითია, უარყოფითია თუ 0-ია
while True:
    number = int(input("შეიყვანეთ რიცხვი: "))
    if number == 0:
        print("რიცხვი არის 0")
        break
    else:
        print(f" რიცხვი არის {number}")

